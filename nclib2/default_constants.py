def geostationary_projection(subsatellite_point, false_easting=0., false_northing=0., perspective_point_height=35785863,
                             semi_major_axis=6378137., semi_minor_axis=6356752.3):
    """Function creating geostationary projection dict.
    As such a dict cannot be costat (varies from satellite to satellite (maybe with time for the same satellite), this
    method ease its creation according CF-1.6 convention.
    Read more here: http://cfconventions.org/cf-conventions/v1.6.0/cf-conventions.html#_vertical_perspective
    :param subsatellite_point: longitude of the satellite (we assume latitude =0. =equator)
    :param false_easting: (number) The value added to all abscissa values in the rectangular coordinates for a map
    projection. This value frequently is assigned to eliminate negative numbers. Expressed in the unit of the coordinate
     variable identified by the standard name projection_x_coordinate.
    :param false_northing: (number) The value added to all ordinate values in the rectangular coordinates for a map
    projection. This value frequently is assigned to eliminate negative numbers. Expressed in the unit of the coordinate
     variable identified by the standard name projection_y_coordinate.
    :param perspective_point_height: actual height of the satellite; default = ideal conditions
    :param semi_major_axis: defines orbital ellipse of the satellite; default = ideal conditions
    :param semi_minor_axis: defines orbital ellipse of the satellite; default = ideal conditions
    :return: geostationary projection dict"""
    return {"long_name": "Parameters of geostationary",
            "datatype": "f8",  # Required
            "dimensions": (),
            "_FillValue": -999.,
            "units": "degree",

            "proj_name": "GEOS141",  # this parameters are understood by QGIS
            "semi_major_axis": semi_major_axis,
            "semi_minor_axis": semi_minor_axis,

            # this parameter defines everything solely; used by QGIS *PRIMARILY* for projection detection
            "spatial_ref": ('PROJCS["unnamed",GEOGCS["unnamed ellipse",DATUM["unknown",SPHEROID["unnamed",6378137,'
                            '298.2570248822731]],PRIMEM["Greenwich",0],UNIT["degree",0.0174532925199433]],PROJECTION['
                            '"Geostationary_Satellite"],PARAMETER["central_meridian",{}],PARAMETER["satellite_height",'
                            '35785863],PARAMETER["false_easting",{}],PARAMETER["false_northing",{}],UNIT["Meter",1]]'
                            ).format(subsatellite_point, false_easting, false_northing),

            "grid_mapping_name": "geostationary",
            "longitude_of_projection_origin": subsatellite_point,
            "latitude_of_projection_origin": 0.,
            "perspective_point_height": perspective_point_height,
            "false_easting": false_easting,
            "false_northing": false_northing,
           }


